#!/bin/bash
set -e

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "コンテナ初期化スクリプト (post-create)"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# --------------------------------------------------------------------------
# システムパッケージ（LibreOffice + Poppler）
# --------------------------------------------------------------------------
echo "システムパッケージのインストール..."
sudo apt-get update
sudo apt-get install -y --no-install-recommends \
  libreoffice-impress \
  poppler-utils
sudo apt-get clean
sudo rm -rf /var/lib/apt/lists/*
echo "LibreOffice + Poppler installed"

# --------------------------------------------------------------------------
# Python パッケージ（pptx スキル用）
# --------------------------------------------------------------------------
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "Python パッケージのインストール"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

pip install "markitdown[pptx]" Pillow
echo "markitdown + Pillow installed"

# --------------------------------------------------------------------------
# Node.js パッケージ（PptxGenJS）
# --------------------------------------------------------------------------
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "Node.js パッケージのインストール"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

npm install -g pptxgenjs
echo "pptxgenjs installed"

# --------------------------------------------------------------------------
# 外部スキル（Proprietary ライセンスのため Git 管理不可）
# --------------------------------------------------------------------------
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "外部スキルのインストール"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

npx skills add https://github.com/anthropics/skills --skill pptx -g -y
echo "pptx skill installed"

echo ""
echo "コンテナ作成完了！！"
